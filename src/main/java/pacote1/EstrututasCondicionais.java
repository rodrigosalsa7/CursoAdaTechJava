package pacote1;

public class EstrututasCondicionais {
    public static void main(String[] args) {
        int nota = 70;

        // nota >= 70, o aluno está aprovado.

        if (nota >= 70){
            System.out.println("Aluno aprovado.");
        } else {
            System.out.println("Aluno não aprovado.");
        }
    }
}
